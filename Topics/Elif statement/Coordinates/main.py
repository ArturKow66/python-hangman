x_value = float(input())
y_value = float(input())

if x_value == 0.0 == y_value:
    print("It's the origin!")
elif x_value == 0.0 or y_value == 0.0:
    print("One of the coordinates is equal to zero!")
elif x_value > 0.0 < y_value:
    print('I')
elif x_value < 0.0 < y_value:
    print('II')
elif x_value < 0.0 > y_value:
    print('III')
else:
    print('IV')
